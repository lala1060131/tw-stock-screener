def get_zh_name(symbol):
    """取得股票中文名稱，找不到回傳 None"""
    return TW_NAME_MAP.get(str(symbol).strip(), (None, None, None))[0]

def get_zh_sector(symbol):
    """取得股票中文產業"""
    info = TW_NAME_MAP.get(str(symbol).strip())
    return info[1] if info else None

def get_zh_subsector(symbol):
    """取得股票中文子產業"""
    info = TW_NAME_MAP.get(str(symbol).strip())
    return info[2] if info else None

def translate_sector(en_sector):
    """英文產業名稱轉中文"""
    if not en_sector:
        return "未分類"
    for k, v in SECTOR_ZH.items():
        if k.lower() in en_sector.lower():
            return v
    return en_sector

def enrich_with_zh(result: dict) -> dict:
    """
    將篩選結果 dict 加入中文名稱欄位
    name_zh:      中文名稱
    sector_zh:    中文產業
    subsector_zh: 中文子產業
    display_name: 優先顯示中文，次選英文
    """
    sym = result.get("symbol", "")
    zh  = get_zh_name(sym)
    sec_zh = get_zh_sector(sym) or translate_sector(result.get("sector", ""))
    sub_zh = get_zh_subsector(sym)

    result["name_zh"]      = zh or ""
    result["sector_zh"]    = sec_zh or ""
    result["subsector_zh"] = sub_zh or ""
    result["display_name"] = zh if zh else (result.get("name") or sym)
    return result
